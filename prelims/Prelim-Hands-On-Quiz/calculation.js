function add(a, b, c, d) {
  return a + b + c + d;
}

function subtract(a, b) {
  return a - b;
}

function multiplication(a, b) {
  return a * b;
}

function multiply(a, b, c) {
  return a * b * c;
}

function divide(a, b) {
  return a / b;
}

module.exports = { add, subtract, multiplication, multiply, divide };
